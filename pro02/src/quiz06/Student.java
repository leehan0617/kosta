package quiz06;

public class Student {
	private int hakbunho;
	private String name;
	private String major;
	
	public int getHakbunho() {
		return hakbunho;
	}
	
	public void setHakbunho(int hakbunho) {
		this.hakbunho = hakbunho;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getMajor() {
		return major;
	}
	
	public void setMajor(String major) {
		this.major = major;
	}
}
